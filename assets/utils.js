/* L-TEX Utilities — data mapping, price formatting, slug helpers
   Maps existing window.PRODUCTS schema to the new design.
*/
window.LTEX = window.LTEX || {};

(() => {
  const L = window.LTEX;

  /* === Configuration === */
  L.CONFIG = {
    EUR_UAH_RATE: 43,                // EUR → UAH
    PER_PAGE: 30,
    IMAGES_DIR: window.IMAGES_DIR || '2025-2026-named-top5',
    PHONES: [
      { display: '+380 67 671 05 16', tel: '+380676710516' },
      { display: '+380 99 358 49 92', tel: '+380993584992' },
    ],
    EMAIL: 'ltex.lutsk.ai@gmail.com',
    TELEGRAM: 'https://t.me/L_TEX',
    ADDRESS: 'с. Піддубці, Луцький р-н, Волинська обл.',
    MIN_ORDER_KG: 10,
    BRAND: 'L-TEX',
    /* Public site URL — used to build absolute URLs for SEO / Open Graph / JSON-LD. */
    SITE_URL: 'https://ltexlutskai-tech.github.io/catalog-full',
  };

  /* Make a relative path absolute against SITE_URL (or location.origin if same host). */
  L.absUrl = (path) => {
    if(!path) return L.CONFIG.SITE_URL;
    if(/^https?:\/\//.test(path)) return path;
    const base = L.CONFIG.SITE_URL.replace(/\/$/, '');
    const p = path.startsWith('/') ? path : '/' + path;
    return base + p;
  };

  /* === Enums / labels === */
  L.QUALITY_LABELS = {
    'Екстра':'Екстра','Крем':'Крем','1й сорт':'1й сорт','2й сорт':'2й сорт',
    'Сток':'Сток','Мікс':'Мікс'
  };
  L.SEASON_LABELS = {
    'Зима':'Зима','Літо':'Літо','Демісезон':'Демісезон','Всесезонне':'Всесезонне'
  };
  L.AUDIENCE_LABELS = {
    'Жіноче':'Жіноча','Чоловіче':'Чоловіча','Дитяче':'Дитяча',
    'Дорослі':'Дорослий','Мікс':'Унісекс'
  };
  L.COUNTRY_FLAG = {
    'Англія':'🇬🇧','Німеччина':'🇩🇪','Канада':'🇨🇦',
    'Польща':'🇵🇱','Шотландія':'🏴󠁧󠁢󠁳󠁣󠁴󠁿','Італія':'🇮🇹','США':'🇺🇸'
  };

  /* === Slug === */
  L.slug = (str) => {
    if(!str) return '';
    const map = {'а':'a','б':'b','в':'v','г':'h','ґ':'g','д':'d','е':'e','є':'ie','ж':'zh','з':'z','и':'y','і':'i','ї':'i','й':'i','к':'k','л':'l','м':'m','н':'n','о':'o','п':'p','р':'r','с':'s','т':'t','у':'u','ф':'f','х':'kh','ц':'ts','ч':'ch','ш':'sh','щ':'shch','ь':'','ю':'iu','я':'ia','ы':'y','э':'e','ё':'e','ъ':''};
    return String(str).toLowerCase().split('').map(c => map[c] !== undefined ? map[c] : c)
      .join('').replace(/[^a-z0-9]+/g,'-').replace(/^-+|-+$/g,'').slice(0,80);
  };

  /* === Image helpers === */
  const encName = (name) => encodeURIComponent(name).replace(/'/g, '%27');
  L.imagesFor = (id) => {
    const list = (window.IMAGES_BY_ID && window.IMAGES_BY_ID[String(id)]) || [];
    return list.map(name => `${L.CONFIG.IMAGES_DIR}/${encName(name)}`);
  };
  /* Thumbnails (~800px) generated by optimize_images.py.
     Same filename, optionally swap .jpg → .webp on supporting browsers. */
  L.thumbsFor = (id) => {
    const list = (window.IMAGES_BY_ID && window.IMAGES_BY_ID[String(id)]) || [];
    return list.map(name => {
      const dot = name.lastIndexOf('.');
      const base = dot > 0 ? name.slice(0, dot) : name;
      return `${L.CONFIG.IMAGES_DIR}/thumbs/${encName(base)}.jpg`;
    });
  };
  L.thumbFor = (id) => L.thumbsFor(id)[0] || L.imagesFor(id)[0] || null;
  L.firstImageFor = (id) => L.imagesFor(id)[0] || null;

  L.detailFor = (id) => (window.DETAILS_BY_ID && window.DETAILS_BY_ID[String(id)]) || '';

  L.parseDetails = (text) => {
    if(!text) return {};
    const out = {};
    text = text.replace(/✔️|✔/g,'').replace(/\r/g,'');
    const lines = text.split('\n').map(l => l.trim()).filter(Boolean);
    for(const line of lines){
      const m = line.match(/^(Сезон|Сорт|Стать|Розміри|Країна|Бренд|Кількість одиниць|Вага одиниці|Вага лота|Наповнення|Категорія)\s*[:\t]\s*(.*)$/i);
      if(m){
        out[m[1].toLowerCase().replace(/\s/g,'_')] = m[2].trim();
      }
    }
    return out;
  };

  /* === Price === */
  /* Parse a weight string like "25", "20-25", "20-25 кг", "0,4-0,5 кг" → average kg.
     Returns 0 if the string can't be interpreted. */
  L.parseAvgWeight = (raw) => {
    if(raw == null) return 0;
    const s = String(raw).replace(',', '.');
    const matches = s.match(/\d+(?:\.\d+)?/g);
    if(!matches || !matches.length) return 0;
    const nums = matches.map(parseFloat).filter(n => !isNaN(n));
    if(!nums.length) return 0;
    if(nums.length === 1) return nums[0];
    /* Use first two numbers as a [min, max] range */
    return (nums[0] + nums[1]) / 2;
  };

  /* Detect the prevailing EUR→UAH rate from lot data (mode = most frequent
     rounded value). Falls back to median, then null. */
  L.detectRateFromLots = () => {
    const map = window.LOTS_DATA;
    if(!map) return null;
    const counts = new Map();
    for(const pid of Object.keys(map)){
      const lots = (map[pid] && map[pid].lots) || [];
      for(const l of lots){
        const r = Number(l.eur_rate);
        if(!r || r <= 0 || r > 200) continue;
        const k = Math.round(r * 100) / 100;
        counts.set(k, (counts.get(k) || 0) + 1);
      }
    }
    if(!counts.size) return null;
    /* Pick the rate with the highest count; tiebreak by the larger value
       (more recent imports tend to use a higher rate). */
    let best = null, bestCount = 0;
    for(const [r, c] of counts){
      if(c > bestCount || (c === bestCount && r > (best || 0))){
        best = r; bestCount = c;
      }
    }
    return best;
  };

  /* User-editable rate.
     Priority: localStorage user override → auto-detected from lots → CONFIG default. */
  const RATE_KEY = 'ltex-rate';
  L.getRate = () => {
    try {
      const v = localStorage.getItem(RATE_KEY);
      const n = v == null ? null : parseFloat(v);
      if(n && n > 0) return n;
    } catch(e){}
    const auto = L.detectRateFromLots();
    if(auto && auto > 0) return auto;
    return L.CONFIG.EUR_UAH_RATE;
  };
  L.getUserRate = () => {
    try {
      const v = localStorage.getItem(RATE_KEY);
      const n = v == null ? null : parseFloat(v);
      return n && n > 0 ? n : null;
    } catch(e){ return null; }
  };
  L.setRate = (n) => {
    n = Number(n);
    if(!n || n <= 0) return false;
    try { localStorage.setItem(RATE_KEY, String(n)); } catch(e){}
    L.recomputePrices();
    window.dispatchEvent(new CustomEvent('ltex:rate-changed', { detail: { rate: n } }));
    return true;
  };
  L.resetRate = () => {
    try { localStorage.removeItem(RATE_KEY); } catch(e){}
    L.recomputePrices();
    window.dispatchEvent(new CustomEvent('ltex:rate-changed', { detail: { rate: L.getRate() } }));
  };
  L.openRateEditor = () => {
    const auto = L.detectRateFromLots();
    const userRate = L.getUserRate();
    const cur = L.getRate();
    const lines = [
      'Курс EUR → UAH (1€ = ? ₴)',
      '',
      auto ? `Автоматично з лотів: ${auto}` : '',
      userRate ? `Поточне (ваше): ${userRate}` : `Поточне: ${cur} (авто)`,
      '',
      'Введіть нове значення (порожньо = повернутися на авто)',
    ].filter(Boolean).join('\n');
    const v = prompt(lines, userRate || cur);
    if(v == null) return;
    if(String(v).trim() === ''){
      if(L.getUserRate()){
        L.resetRate();
        L.toast(`Курс відновлено: 1€ = ${L.getRate()} ₴ (авто)`, 'success');
      }
      return;
    }
    const n = parseFloat(String(v).replace(',', '.'));
    if(isNaN(n) || n <= 0){ L.toast('Невірне значення', 'error'); return; }
    L.setRate(n);
    L.toast(`Курс оновлено: 1€ = ${n} ₴`, 'success');
  };
  L.eurToUah = (eur) => Math.round(eur * L.getRate());
  L.formatUah = (uah) => {
    if(uah == null || isNaN(uah)) return '';
    return Math.round(uah).toLocaleString('uk-UA').replace(/,/g,' ') + ' ₴';
  };
  L.formatEur = (eur) => {
    if(eur == null || isNaN(eur)) return '';
    return '€' + Number(eur).toFixed(2);
  };
  L.priceEurFor = (p) => {
    if(!p) return null;
    return p.akciya != null ? p.akciya : p.price;
  };
  L.hasDiscount = (p) => p && p.akciya != null && p.price != null && p.akciya < p.price;
  L.discountPct = (p) => {
    if(!L.hasDiscount(p)) return 0;
    return Math.round((1 - p.akciya / p.price) * 100);
  };

  /* === Quality / season label colours === */
  L.qualityClass = (q) => {
    const map = {'Екстра':'q-extra','Крем':'q-cream','1й сорт':'q-first','2й сорт':'q-second','Сток':'q-stock','Мікс':'q-mix'};
    return map[q] || '';
  };

  /* === In stock === */
  L.inStock = (p) => Number(p?.qty_stock || 0) > 0 || p?.qty_stock === null /* unknown == in catalog */;

  /* === Top categories === */
  L.TOP_CATS = [
    'Одяг','Взуття','Аксесуари','Дім та побут','Іграшки','Косметика','Bric-a-Brac'
  ];
  L.normalizeTopCat = (cat) => {
    if(!cat) return null;
    const c = String(cat).trim();
    if(L.TOP_CATS.includes(c)) return c;
    /* sub-categories that are stored as top-level "category" — group them under Одяг */
    const odyag = ['Светри та кардигани','Худі та світшоти','Куртки та пальта','Одяг мікс','Футболки','Кофти флісові','Штани та брюки','Нижня білизна','Робочий одяг','Джинси','Спортивний одяг','Піджаки та жилети','Колготки та легінси','Сорочки та блузи','Спідниці та плаття','Шкарпетки','Шорти','Халати та піжами','Купальники','Комбінезони','Вітровки та штормовки','Шапки та головні убори'];
    const vzuttya = ['Взуття мікс','Кросівки та кеди','Туфлі та босоніжки','Черевики та чоботи','Тапочки та шльопанці','Взуття гумове','Взуття робоче'];
    const aks = ['Сумки та рюкзаки','Біжутерія'];
    const dim = ['Постільна білизна','Рушники','Килими та килимки','Побутові товари','Товари для тварин'];
    const igr = ['М’які іграшки','Тверді іграшки'];
    const kos = ['Косметика декоративна'];
    if(odyag.includes(c)) return 'Одяг';
    if(vzuttya.includes(c)) return 'Взуття';
    if(aks.includes(c)) return 'Аксесуари';
    if(dim.includes(c)) return 'Дім та побут';
    if(igr.includes(c)) return 'Іграшки';
    if(kos.includes(c)) return 'Косметика';
    return c;
  };

  /* For each product compute a `topCat` */
  L.enrich = (p) => {
    if(p._enriched) return p;
    p.topCat = L.normalizeTopCat(p.category);
    p.subcat = p.subcategory || (p.topCat !== p.category ? p.category : '');
    p.images = L.imagesFor(p.id);
    p.thumbs = L.thumbsFor(p.id);
    p.image = p.images[0] || null;       // original (high-res) — for lightbox / hero
    p.thumb = p.thumbs[0] || p.image;    // small (~800px) — for cards
    p.avgWeight = L.parseAvgWeight(p.weight);  // numeric avg kg of one lot/mix
    p.priceEur = L.priceEurFor(p);
    p.priceUah = p.priceEur != null ? L.eurToUah(p.priceEur) : null;
    p.oldPriceEur = L.hasDiscount(p) ? p.price : null;
    p.oldPriceUah = p.oldPriceEur != null ? L.eurToUah(p.oldPriceEur) : null;
    p.discount = L.discountPct(p);
    p.inStock = L.inStock(p);
    p.slug = L.slug(p.name) + '-' + p.id;
    p.detailRaw = L.detailFor(p.id);
    p._enriched = true;
    return p;
  };

  L.getProducts = () => {
    const list = window.PRODUCTS || [];
    if(!list._enriched){
      list.forEach(L.enrich);
      /* Mark top 5% of products by ID as "NEW" (proxy for newest additions). */
      const sorted = [...list].sort((a, b) => parseInt(b.id) - parseInt(a.id));
      const top = Math.max(15, Math.floor(sorted.length * 0.05));
      const newIds = new Set(sorted.slice(0, top).map(p => p.id));
      for(const p of list) p.isNew = newIds.has(p.id);
      list._enriched = true;
    }
    return list;
  };

  /* Recompute UAH price columns for all products after rate changes. */
  L.recomputePrices = () => {
    const list = window.PRODUCTS || [];
    for(const p of list){
      if(p.priceEur != null) p.priceUah = L.eurToUah(p.priceEur);
      if(p.oldPriceEur != null) p.oldPriceUah = L.eurToUah(p.oldPriceEur);
    }
  };

  L.findProduct = (idOrSlug) => {
    const list = L.getProducts();
    return list.find(p => p.id === idOrSlug || String(p.id) === String(idOrSlug) || p.slug === idOrSlug);
  };

  /* === Subcategory aggregation === */
  L.subcatsForTop = (top) => {
    const set = new Map();
    for(const p of L.getProducts()){
      if(p.topCat !== top) continue;
      const sc = p.subcat || (p.topCat !== p.category ? p.category : null);
      if(!sc) continue;
      set.set(sc, (set.get(sc) || 0) + 1);
    }
    return [...set.entries()].sort((a,b) => b[1] - a[1]).map(([n,c]) => ({name:n,count:c}));
  };
  L.topCatCounts = () => {
    const map = new Map();
    for(const p of L.getProducts()) map.set(p.topCat, (map.get(p.topCat)||0)+1);
    return map;
  };

  /* === YouTube helpers === */
  L.youtubeId = (url) => {
    if(!url) return null;
    const m = url.match(/(?:youtube\.com\/(?:watch\?v=|embed\/|v\/)|youtu\.be\/)([A-Za-z0-9_-]{11})/);
    if(m) return m[1];
    const list = url.match(/[?&]list=([A-Za-z0-9_-]+)/);
    return list ? { playlist: list[1] } : null;
  };
  L.youtubeThumb = (url) => {
    const id = L.youtubeId(url);
    if(!id) return null;
    if(typeof id === 'string') return `https://i.ytimg.com/vi/${id}/hqdefault.jpg`;
    return null; // playlists don't have a single thumb
  };
  L.youtubeWatchUrl = (url) => {
    const id = L.youtubeId(url);
    if(!id) return url;
    if(typeof id === 'string') return `https://www.youtube.com/watch?v=${id}`;
    return url;
  };

  /* === Debounce === */
  L.debounce = (fn, ms = 200) => {
    let t; return (...args) => { clearTimeout(t); t = setTimeout(() => fn(...args), ms); };
  };

  /* === DOM helpers === */
  L.escapeHtml = (str) => String(str ?? '').replace(/[&<>"']/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
  L.qs = (sel, root = document) => root.querySelector(sel);
  L.qsa = (sel, root = document) => [...root.querySelectorAll(sel)];

  /* === URL state === */
  L.getParam = (key) => new URLSearchParams(location.search).get(key);
  L.getParams = () => Object.fromEntries(new URLSearchParams(location.search));
  L.setParams = (patch, replace = false) => {
    const url = new URL(location.href);
    for(const [k,v] of Object.entries(patch)){
      if(v === null || v === undefined || v === '' || (Array.isArray(v) && v.length === 0)) {
        url.searchParams.delete(k);
      } else if(Array.isArray(v)){
        url.searchParams.set(k, v.join(','));
      } else {
        url.searchParams.set(k, String(v));
      }
    }
    history[replace ? 'replaceState' : 'pushState']({}, '', url);
    window.dispatchEvent(new CustomEvent('ltex:urlchange'));
  };
  L.getMulti = (key) => {
    const v = L.getParam(key);
    return v ? v.split(',').filter(Boolean) : [];
  };

  /* === Toast === */
  let toastTimer;
  L.toast = (msg, type = 'info') => {
    let el = document.getElementById('ltex-toast');
    if(!el){
      el = document.createElement('div');
      el.id = 'ltex-toast';
      el.className = 'toast';
      document.body.appendChild(el);
    }
    el.textContent = msg;
    el.classList.add('show');
    if(type === 'error') el.style.background = 'var(--red-600)';
    else if(type === 'success') el.style.background = 'var(--primary)';
    else el.style.background = 'var(--gray-900)';
    clearTimeout(toastTimer);
    toastTimer = setTimeout(() => el.classList.remove('show'), 2200);
  };

  /* === Build catalog href === */
  L.catalogHref = (params = {}) => {
    const url = new URL('catalog.html', location.href);
    for(const [k,v] of Object.entries(params)){
      if(v != null && v !== '') {
        url.searchParams.set(k, Array.isArray(v) ? v.join(',') : String(v));
      }
    }
    return url.pathname.split('/').pop() + (url.search || '');
  };
  L.productHref = (id) => `product.html?id=${encodeURIComponent(id)}`;

  /* === Lot helpers (works with window.LOTS_DATA from data/lots.js) === */
  L.lotsForProduct = (prodId) => {
    const lots = window.LOTS_DATA && window.LOTS_DATA[String(prodId)];
    return lots ? lots.lots || [] : [];
  };
  L.hasLots = (prodId) => L.lotsForProduct(prodId).length > 0;
  L.getAllLots = () => {
    const map = window.LOTS_DATA || {};
    const out = [];
    for(const pid of Object.keys(map)){
      const grp = map[pid];
      for(const lot of (grp.lots || [])){
        /* enrich with parent context */
        out.push({
          ...lot,
          _grp: grp,
          parentProd: L.findProduct(pid),
        });
      }
    }
    return out;
  };
  L.lotByBarcode = (barcode) => {
    const all = L.getAllLots();
    return all.find(l => String(l.barcode) === String(barcode)) || null;
  };

  /* Clean lot description text from generator artefacts */
  L.cleanLotDesc = (desc) => {
    if(!desc) return '';
    let t = String(desc);
    t = t.replace(/¶/g, '\n');
    t = t.replace(/\\n/g, '\n');
    t = t.replace(/ /g, ' ');
    t = t.replace(/\s+\n/g, '\n').replace(/\n\s+/g, '\n').trim();
    return t;
  };

  /* === Order request — reusable Telegram + Email targets === */
  L.tgOrderUrl = (text) => `${L.CONFIG.TELEGRAM}?text=${encodeURIComponent(String(text).slice(0, 1000))}`;
  L.mailOrderUrl = (subject, text) => `mailto:${L.CONFIG.EMAIL}?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(text)}`;

  L.lotHref = (barcode) => `lots.html?barcode=${encodeURIComponent(barcode)}`;
  L.lotsForProductHref = (prodId) => `lots.html?id=${encodeURIComponent(prodId)}`;

  /* === sortMatch (relevance heuristic) === */
  L.byRelevance = (a, b, query) => {
    if(!query) return 0;
    const q = query.toLowerCase();
    const ai = a.name.toLowerCase().indexOf(q);
    const bi = b.name.toLowerCase().indexOf(q);
    if(ai !== bi){
      if(ai === -1) return 1;
      if(bi === -1) return -1;
      return ai - bi;
    }
    return 0;
  };
})();
