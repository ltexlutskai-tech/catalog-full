  /* === Slug === */
  L.slug = (str) => {
    if(!str) return '';
    const map = {'а':'a','б':'b','в':'v','г':'h','ґ':'g','д':'d','е':'e','є':'ie','ж':'zh','з':'z','и':'y','і':'i','ї':'i','й':'i','к':'k','л':'l','м':'m','н':'n','о':'o','п':'p','р':'r','с':'s','т':'t','у':'u','ф':'f','х':'kh','ц':'ts','ч':'ch','ш':'sh','щ':'shch','ь':'','ю':'iu','я':'ia','ы':'y','э':'e','ё':'e','ъ':''};
    return String(str).toLowerCase().split('').map(c => map[c] !== undefined ? map[c] : c)
      .join('').replace(/[^a-z0-9]+/g,'-').replace(/^-+|-+$/g,'').slice(0,80);
  };

  /* === Image helpers === */
  const encName = (name) => encodeURIComponent(name).replace(/'/g, '%27');
  L.imagesFor = (id) => {
    const list = (window.IMAGES_BY_ID && window.IMAGES_BY_ID[String(id)]) || [];
    return list.map(name => `${L.CONFIG.IMAGES_DIR}/${encName(name)}`);
  };
  /* Thumbnails (~800px) generated by optimize_images.py.
     Same filename, optionally swap .jpg → .webp on supporting browsers. */
  L.thumbsFor = (id) => {
    const list = (window.IMAGES_BY_ID && window.IMAGES_BY_ID[String(id)]) || [];
    return list.map(name => {
      const dot = name.lastIndexOf('.');
      const base = dot > 0 ? name.slice(0, dot) : name;
      return `${L.CONFIG.IMAGES_DIR}/thumbs/${encName(base)}.jpg`;
    });
  };
  L.thumbFor = (id) => L.thumbsFor(id)[0] || L.imagesFor(id)[0] || null;
  L.firstImageFor = (id) => L.imagesFor(id)[0] || null;

  /* Build the attribute string for a card <img> that falls back from thumb to
     full-size original if the 800px thumbnail is missing (covers freshly
     uploaded photos that haven't been processed by optimize_images.py yet). */
  L.imgAttrs = (thumb, original, alt) => {
    const src = thumb || original || '';
    const fb = (original && original !== thumb) ? original : '';
    const altEsc = L.escapeHtml(alt || '');
    if(!fb) return `src="${src}" alt="${altEsc}" loading="lazy" decoding="async"`;
    return `src="${src}" data-fallback="${fb}" alt="${altEsc}" loading="lazy" decoding="async" onerror="if(this.dataset.fallback&&this.src!==this.dataset.fallback){this.src=this.dataset.fallback;}"`;
  };

  L.detailFor = (id) => (window.DETAILS_BY_ID && window.DETAILS_BY_ID[String(id)]) || '';

  L.parseDetails = (text) => {
    if(!text) return {};
    const out = {};
    text = text.replace(/✔️|✔/g,'').replace(/\r/g,'');
    const lines = text.split('\n').map(l => l.trim()).filter(Boolean);
    for(const line of lines){
      const m = line.match(/^(Сезон|Сорт|Стать|Розміри|Країна|Бренд|Кількість одиниць|Вага одиниці|Вага лота|Наповнення|Категорія)\s*[:\t]\s*(.*)$/i);
      if(m){
        out[m[1].toLowerCase().replace(/\s/g,'_')] = m[2].trim();
      }
    }
    return out;
  };

  /* === Price === */
  /* Parse a weight/quantity string like "25", "20-25", "20-25 кг", "0,4-0,5 кг"
     → average value. Returns 0 if the string can't be interpreted.
     Parenthetical notes ("(в середньому)") are dropped so they can't be picked
     up as stray numbers. */
  L.parseAvgWeight = (raw) => {
    if(raw == null) return 0;
    const s = String(raw).replace(/\([^)]*\)/g, ' ').replace(/,/g, '.');
    const matches = s.match(/\d+(?:\.\d+)?/g);
    if(!matches || !matches.length) return 0;
    const nums = matches.map(parseFloat).filter(n => !isNaN(n));
    if(!nums.length) return 0;
    if(nums.length === 1) return nums[0];
    /* Use first two numbers as a [min, max] range */
    return (nums[0] + nums[1]) / 2;
  };

  /* Normalise a messy "Вага лота" source string into a clean "X кг" / "X-Y кг"
     label. The price-list "Опис" column has typos and stray units
     ("15-25 ru", "50-100кг", "10 кг (в середньому)"), so we keep only the
     numeric range and re-attach the kg unit. Returns '' when there is no
     usable number ("-", "—", blank, junk). */
  L.formatLotWeight = (raw) => {
    if(raw == null) return '';
    const nums = String(raw).replace(/\([^)]*\)/g, ' ').match(/\d+(?:[.,]\d+)?/g);
    if(!nums || !nums.length) return '';
    const range = nums.length >= 2 ? `${nums[0]}-${nums[1]}` : nums[0];
    return `${range} кг`;
  };

  /* Same idea for a per-lot piece count ("Кількість одиниць") → "X шт" /
     "X-Y шт". A "шт/кг" value is a density (pieces per kg), not a per-lot
     count, so it is rejected. Returns '' when there is no usable number. */
  L.formatLotCount = (raw) => {
    if(raw == null) return '';
    if(/\/\s*кг/i.test(String(raw))) return '';
    const nums = String(raw).replace(/\([^)]*\)/g, ' ').match(/\d+(?:[.,]\d+)?/g);
    if(!nums || !nums.length) return '';
    const range = nums.length >= 2 ? `${nums[0]}-${nums[1]}` : nums[0];
    return `${range} шт`;
  };

  /* Ukrainian plural for "лот". */
  L.lotWord = (n) => {
    const t = Math.abs(n) % 100;
    if(t >= 11 && t <= 14) return 'лотів';
    const o = Math.abs(n) % 10;
    if(o === 1) return 'лот';
    if(o >= 2 && o <= 4) return 'лоти';
    return 'лотів';
  };

  /* We sell by lots (mishky). Quantity inputs across the site count LOTS, and
     each lot holds ~`p.lotSize` of the product's base unit (kg or pieces).
     Price for `qty` lots = priceUnit × lotSize × qty. When the per-lot size is
     unknown the multiplier is 1 (qty falls back to counting the base unit). */
  L.lotMultiplier = (p) => (p && p.lotSize > 0) ? p.lotSize : 1;

  /* Estimated total kg for `qty` lots of product p. */
  L.lotKg = (p, qty) => {
    if(!p) return 0;
    const q = Number(qty) || 0;
    if(p.lotUnit === 'шт') return (p.lotSize || 0) * (p.unitWeight || 0) * q; // pieces × kg/piece
    return (p.lotSize > 0 ? p.lotSize : 1) * q;                              // kg/lot × lots
  };

  /* Detect the prevailing EUR→UAH rate from lot data (mode = most frequent
     rounded value). Falls back to median, then null. */
  L.detectRateFromLots = () => {
    const map = window.LOTS_DATA;
    if(!map) return null;
    const counts = new Map();
    for(const pid of Object.keys(map)){
      const lots = (map[pid] && map[pid].lots) || [];
      for(const l of lots){
        const r = Number(l.eur_rate);
        if(!r || r <= 0 || r > 200) continue;
        const k = Math.round(r * 100) / 100;
        counts.set(k, (counts.get(k) || 0) + 1);
      }
    }
    if(!counts.size) return null;
    /* Pick the rate with the highest count; tiebreak by the larger value
       (more recent imports tend to use a higher rate). */
    let best = null, bestCount = 0;
    for(const [r, c] of counts){
      if(c > bestCount || (c === bestCount && r > (best || 0))){
        best = r; bestCount = c;
      }
    }
    return best;
  };

